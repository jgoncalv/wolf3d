#include "libft.h"

intmax_t	ft_atoi(const char *str)
{
	int			i;
	intmax_t	res;
	int			sign;

	i = 0;
	res = 0;
	sign = 1;
	while (str[i] <= ' ' && str[i] >= 0)
		i++;
	if (str[i] == '-')
	{
		sign *= -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (ft_isdigit(str[i]))
	{
		res = (res * 10) + (str[i] - '0');
		i++;
	}
	return (res * sign);
}
